        v NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v
        FROM medios_transporte
        WHERE id_medio_transporte = p_id_medio;
        RETURN v;
    END;

    ---INSERTAR
    PROCEDURE pr_insertar_alerta(
        p_id_usuario NUMBER,
        p_id_tipo_alerta NUMBER,
        p_id_barrio NUMBER,
        p_id_tipo_arma NUMBER,
        p_id_medio_transporte NUMBER,
        p_etapa VARCHAR2,
        p_sector VARCHAR2,
        p_manzana VARCHAR2,
        p_casa VARCHAR2,
        p_calle VARCHAR2,
        p_carrera VARCHAR2,
        p_referencia VARCHAR2,
        p_latitud NUMBER,
        p_longitud NUMBER,
        p_descripcion VARCHAR2
    )
    AS
    BEGIN

        IF fx_usuario_existe(p_id_usuario) = 0 THEN
            DBMS_OUTPUT.PUT_LINE('Usuario no existe'); RETURN;
        END IF;

        IF fx_tipo_alerta_existe(p_id_tipo_alerta) = 0 THEN
            DBMS_OUTPUT.PUT_LINE('Tipo alerta no existe'); RETURN;
        END IF;

        IF fx_barrio_existe(p_id_barrio) = 0 THEN
            DBMS_OUTPUT.PUT_LINE('Barrio no existe'); RETURN;
        END IF;

        IF p_id_tipo_arma IS NOT NULL AND fx_tipo_arma_existe(p_id_tipo_arma) = 0 THEN
            DBMS_OUTPUT.PUT_LINE('Tipo arma no existe'); RETURN;
        END IF;

        IF p_id_medio_transporte IS NOT NULL AND fx_medio_existe(p_id_medio_transporte) = 0 THEN
            DBMS_OUTPUT.PUT_LINE('Medio transporte no existe'); RETURN;
        END IF;

        INSERT INTO alertas (
            id_usuario,
            id_tipo_alerta,
            id_barrio,
            id_tipo_arma,
            id_medio_transporte,
            estado,
            etapa,
            sector,
            manzana,
            casa,
            calle,
            carrera,
            referencia,
            latitud,
            longitud,
            descripcion,
            fecha
        )
        VALUES (
            p_id_usuario,
            p_id_tipo_alerta,
            p_id_barrio,
            p_id_tipo_arma,
            p_id_medio_transporte,
            'PENDIENTE',
            p_etapa,
            p_sector,
            p_manzana,
            p_casa,
            p_calle,
            p_carrera,
            p_referencia,
            p_latitud,
            p_longitud,
            p_descripcion,
            SYSTIMESTAMP
        );

        DBMS_OUTPUT.PUT_LINE('Alerta creada correctamente');
        COMMIT;

    END;

